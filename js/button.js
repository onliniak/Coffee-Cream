// Mobile menu via javascript

function mobinu() {
  var x = document.getElementById("mobinu");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}

// Sidebar show/hide

function sidiBer() {
  var x = document.getElementById("sidiBer");
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "block";
  }
}